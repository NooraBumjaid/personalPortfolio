/** Arabic number–noun agreement: dual (2), plural (3–10), singular (1 and 11+). */
function arCount(
  n: number,
  forms: { one: string; two: string; few: string; many: string }
): string {
  if (n === 1) return `1 ${forms.one}`;
  if (n === 2) return `2 ${forms.two}`;
  if (n >= 3 && n <= 10) return `${n} ${forms.few}`;
  return `${n} ${forms.many}`;
}

